public class Calculator {

    public  int sum(int a, int b){
        return a+b;
    }

    public  int div(int a, int b){
        return a/b;
    }

    public double divDouble(double a, double b){
        return a/b;
    }

    public double sqr_t(double a){
        return Math.sqrt(a);
    }

}
